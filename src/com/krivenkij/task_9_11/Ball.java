package com.krivenkij.task_9_11;

import java.awt.*;

public class Ball {

    private final Color color;
    private final int weight;

    public Ball(Color color, int weight) {
        this.color = color;
        this.weight = weight;
    }

    public Color getColor() {
        return color;
    }

    public int getWeight() {
        return weight;
    }

}
